using UnityEngine;
using Unity.Mathematics;

using Random = UnityEngine.Random;


/// <summary>
/// spawns a swarm of enemy entities offscreen, encircling the player
/// </summary>
public class EnemySpawnerNonECS : MonoBehaviour
{
    [Header("Spawner")]
    // number of enemies generated per interval
    [SerializeField] private int spawnCount = 30;

    // time between spawns
    [SerializeField] private float spawnInterval = 3f;

    // enemies spawned on a circle of this radius
    [SerializeField] private float spawnRadius = 30f;

    // increase enemy count each wave by this increment
    [SerializeField] int difficultyBonus = 5;

    [Header("Enemy")]
    // random speed range
    [SerializeField] float minSpeed = 4f;
    [SerializeField] float maxSpeed = 12f;
    [SerializeField] GameObject enemyPrefabNonECS;

    // counter
    private float spawnTimer;

    // flag from GameManager to enable spawning
    private bool canSpawn;

    private void Start()
    {
        SpawnWave();
    }

    // get a random point on a circle with given radius
    private float3 RandomPointOnCircle(float radius)
    {
        Vector2 randomPoint = Random.insideUnitCircle.normalized * radius;

        // return random point on circle, centered around the player position
        return new float3(randomPoint.x, 0.5f, randomPoint.y) + (float3)GameManagerNonECS.GetPlayerPosition();
    }

    // spawn one wave of enemies surrounding the player, increasing the count each wave
    private void SpawnWave()
    {


        for (int i = 0; i < spawnCount; i++)
        {
            if (enemyPrefabNonECS != null)
            {
                Vector3 spawnPoint = RandomPointOnCircle(spawnRadius);
                GameObject enemyInstance = Instantiate(enemyPrefabNonECS, spawnPoint, quaternion.identity);

                EnemyNonECS enemyNonECS = enemyInstance.GetComponent<EnemyNonECS>();
                enemyNonECS.SetMoveSpeed(Random.Range(minSpeed, maxSpeed));
            }
        }
        spawnCount += difficultyBonus;
    }

    // signal from GameManager to begin spawning
    public void StartSpawn()
    {
        canSpawn = true;
    }

    private void Update()
    {
        // disable if the game has just started or if player is dead
        if (!canSpawn || GameManagerNonECS.IsGameOver())
        {
            return;
        }

        // count up until next spawn
        spawnTimer += Time.deltaTime;

        // spawn and reset timer
        if (spawnTimer > spawnInterval)
        {
            SpawnWave();
            spawnTimer = 0;
        }
    }
}
